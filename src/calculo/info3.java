      public void scaleImage(){
      
        
        ImageIcon icon = new javax.swing.ImageIcon(getClass().getResource("/calculo/resources/fondo4.jpg"));
        Image img = icon.getImage();
        Image imgScale = img.getScaledInstance(jLabel1.getWidth() , jLabel1.getHeight() , Image.SCALE_SMOOTH);
        ImageIcon ScaledIcon = new ImageIcon(imgScale);
        jLabel1.setIcon(ScaledIcon);
       
        ImageIcon icon2 = new javax.swing.ImageIcon(getClass().getResource("/calculo/resources/ebo.png"));
        Image img2 = icon2.getImage();
        Image imgScale2 = img2.getScaledInstance(jLabel2.getWidth() , jLabel2.getHeight() , Image.SCALE_SMOOTH);
        ImageIcon ScaledIcon2 = new ImageIcon(imgScale2);
        jLabel2.setIcon(ScaledIcon2);
       
    }

